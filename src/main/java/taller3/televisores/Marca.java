package taller3.televisores;

public class Marca {
	//Atributo
	private String nombre;
	
	//Constructor
	public Marca(String nombre) {
		this.nombre = nombre;
	}
	
	// Getter y setter
	public String getNombre() {
		return this.nombre;
	}
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
	
